package main;

public class Celda {
	private int X;
	private int Y;

	public Celda(int x, int y) {
		setX(x);
		setY(y);
	}

	public int getX() {
		return X;
	}
	public void setX(int x) {
		X = x;
	}
	public int getY() {
		return Y;
	}
	public void setY(int y) {
		Y = y;
	}

}
